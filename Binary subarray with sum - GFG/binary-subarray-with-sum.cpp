class Solution{
  public:
    int numberOfSubarrays(vector<int>& arr, int N, int target){
        // code here
        unordered_map<int,int>mpp({{0,1}});
        int count=0,ones=0;
        for(auto x:arr)
        {
            count+=x;
            ones+=mpp[count-target];
            mpp[count]++;
        }
        return ones;
    }
};

